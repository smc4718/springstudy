package com.gdu.app02.anno01;

public class Calculator {
  
  public void add(double a, double b) {
    return a + b;
  }
  public void sub(double a, double b) {
    return a - b;
  }
  public void mul(double a, double b) {
    return a * b;
  }
  public void div(double a, double b) {
    return a / b;
  }
  
  
}
